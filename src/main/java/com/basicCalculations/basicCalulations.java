package com.basicCalculations;

public class basicCalulations {

    public static void main(String[] args) {

        int x=20;
        int y=15;

        int sum=x+y;

        int diff=x-y;

        int product = x*y;

        int quotient=x/y;

        int remainder=x%y;

        System.out.println("Sum of x and y is: "+sum);

        System.out.println("Difference of x and y is: "+diff);

        System.out.println("Product of x and y is: "+product);

        System.out.println("Quotient of x and y is: "+quotient);

        System.out.println("Remainder of x and y is: "+remainder);

    }

}
